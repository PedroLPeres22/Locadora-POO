    private void botaoEnviarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_botaoEnviarActionPerformed
        // Pega os dados dos campos
        String nome = tfNome.getText();
        String desenvolvedora = tfDesenvolvedora.getText();
        String plataforma = tfPlataforma.getText();
        String publicadora = tfPlataforma.getText();
        int preco = Integer.parseInt(jTextField1.getText());
        String classificacao = getClassificacaoSelecionada(grupoBotoesClassificacao);

        // Verifica se os campos estão vazios
        if (nome.isEmpty() || desenvolvedora.isEmpty() || plataforma.isEmpty()
                || publicadora.isEmpty() || classificacao == null) {
            JOptionPane.showMessageDialog(fomularioContainer, "Por favor, preencha todos os campos.");
            return;
        }

        // Cria o objeto Jogo
        Game jogo = new Game(
                nome,
                ferramentas.GeradorID.gerarID(), // código id automatico
                classificacao,
                publicadora,
                desenvolvedora,
                plataforma,
                false, // está alugado
                0, // alugueis
                1, // estoque (pode ser inicializado com 1 cópia)
                preco);

        // Salva o jogo no banco de dados
        ferramentas.CrudProduto.criarProduto(jogo, "jogos");

        JOptionPane.showMessageDialog(fomularioContainer, "Jogo " + nome + " Criado");
    }// GEN-LAST:event_botaoEnviarActionPerformed

    private String getClassificacaoSelecionada(ButtonGroup g) {
        for (Enumeration<AbstractButton> buttons = g.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();

            if (button.isSelected()) {
                return button.getText();
            }
        }
        return null;
    }

